function updateContent(language) {
  // Select the elements where you want to display the content
  const companyInfoElement = document.getElementById("companyInfo");
  const philosophyElement = document.getElementById("philosophy");
  const companyTitleElement = document.getElementById("companyTitle");
  const philoTitleElement = document.getElementById("philoTitle");
  const firstItemElement = document.getElementById("firstItem");
  const secondItemElement = document.getElementById("secondItem");
  const thirdItemElement = document.getElementById("thirdItem");
  const firstItemElementPara = document.getElementById("firstItemPara");
  const secondItemElementPara = document.getElementById("secondItemPara");
  const thirdItemElementPara = document.getElementById("thirdItemPara");
  const homeLink = document.getElementById("homeLink");
  const productsLink = document.getElementById("productsLink");
  const companyLink = document.getElementById("companyLink");
  const contactLink = document.getElementById("contactLink");

  // Check the selected language
  const selectedLanguage = text[language];

  // Update the content

  companyTitleElement.innerHTML = `
    <div class="section-heading">
      <div class="line-dec"></div>
      <h2>${selectedLanguage.companyTitle}</h2>
    </div>
    `;
  companyInfoElement.innerHTML = `	
      
  
      <div id="companyInfo">
      <p>${selectedLanguage.companyInfo}</p>
        </div>`;

  philoTitleElement.innerHTML = `
      <div class="section-heading">
        <div class="line-dec"></div>
        <h2>${selectedLanguage.philosophyTitle}</h2>
      `;
  philosophyElement.innerHTML = `
        
       
  
      </div> <div id="companyInfo">
        <p>${selectedLanguage.philosophy}</p>
          </div>`;

  firstItemElement.textContent = selectedLanguage.gourmetTitle;
  secondItemElement.textContent = selectedLanguage.traditionalSweetsTitle;
  thirdItemElement.textContent = selectedLanguage.chutneyTitle;
  firstItemElementPara.textContent = selectedLanguage.gourmetText;
  secondItemElementPara.textContent = selectedLanguage.traditionalSweetsText;
  thirdItemElementPara.textContent = selectedLanguage.chutneyText;
  homeLink.textContent = selectedLanguage.home;
  productsLink.textContent = selectedLanguage.products;
  companyLink.textContent = selectedLanguage.company;
  contactLink.textContent = selectedLanguage.contact;
}

document.addEventListener("DOMContentLoaded", function () {
  const toggleLanguageButton = document.getElementById("toggleLanguageCompany");

  let currentLanguage = "en"; // Default to English

  toggleLanguageButton.addEventListener("click", function () {
    // Toggle between English and Greek
    currentLanguage = currentLanguage === "en" ? "el" : "en";

    // Call the updateContent function with the selected language
    updateContent(currentLanguage);
  });

  // Initialize content with the default language
  updateContent(currentLanguage);
});
